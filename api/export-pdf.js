function buildCategorySummary(doc, items, totals, globals) {
  let y = sectionTitle(doc, CT, 'Cost Summary by Category');
  y += 4;

  const bsf = N(globals.buildingSF) || 1;
  const grandMid = totals.full.m.tot;

  // Compute per-category raw totals
  const catMap = {};
  for (const it of items) {
    if (!catMap[it.category]) catMap[it.category] = { l: 0, m: 0, h: 0 };
    catMap[it.category].l += itemLow(it);
    catMap[it.category].m += itemMid(it);
    catMap[it.category].h += itemHigh(it);
  }

  const factor = totals.factor;
  const cols = [
    { key: 'cat',  label: 'Category',      width: 165, align: 'left' },
    { key: 'low',  label: 'Low Total',      width: 80,  align: 'right' },
    { key: 'mid',  label: 'Mid Total',      width: 80,  align: 'right' },
    { key: 'high', label: 'High Total',     width: 80,  align: 'right' },
    { key: 'psf',  label: '$/SF (Mid)',     width: 62,  align: 'right' },
    { key: 'pct',  label: '% of Total',     width: 65,  align: 'right' },
  ];

  const rows = Object.entries(catMap).map(([cat, t]) => {
    const scaledMid = t.m * factor;
    const fullMid   = applyMarkups(scaledMid, globals).tot;
    return {
      cat,
      low:  $c(applyMarkups(t.l * factor, globals).tot),
      mid:  $c(fullMid),
      high: $c(applyMarkups(t.h * factor, globals).tot),
      psf:  $sf(fullMid / bsf),
      pct:  grandMid > 0 ? ((fullMid / grandMid) * 100).toFixed(1) + '%' : '—',
    };
  });

  rows.push({ _type: 'total',
    cat: 'TOTAL PROJECT COST',
    low:  $c(totals.full.l.tot),
    mid:  $c(totals.full.m.tot),
    high: $c(totals.full.h.tot),
    psf:  $sf(totals.full.m.tot / bsf),
    pct:  '100.0%',
  });

  y = drawTable(doc, ML, y, cols, rows);
}

