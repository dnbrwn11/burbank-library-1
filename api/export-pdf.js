// ── Generic table renderer ────────────────────────────────────────────────────
// columns: [{key, label, width, align}]
// rows:    [{...data, _type?: 'category'|'subtotal'|'total'}]
// Returns new y.
function drawTable(doc, x, startY, columns, rows, opts = {}) {
  const ROW_H  = opts.rowH || 17;
  const HDR_H  = opts.hdrH || 20;
  const PAD    = opts.pad  || 5;
  const FS     = opts.fs   || 7.5;
  const totalW = columns.reduce((s, c) => s + c.width, 0);

  function drawHeader(y) {
    doc.rect(x, y, totalW, HDR_H).fill(GRAPHITE);
    let cx = x;
    for (const col of columns) {
      doc.fillColor('white').font('Helvetica-Bold').fontSize(FS - 0.5)
         .text(col.label, cx + PAD, y + (HDR_H - FS + 1) / 2,
               { width: col.width - PAD * 2, align: col.align || 'left', lineBreak: false });
      cx += col.width;
    }
    return y + HDR_H;
  }

  function drawRow(y, row, isOdd) {
    const isCategory = row._type === 'category';
    const isSubtotal = row._type === 'subtotal';
    const isTotal    = row._type === 'total';
    const isSpacer   = row._type === 'spacer';

    if (isSpacer) { return y + 6; }

    const bg   = isCategory ? '#3A3A3A'
               : isTotal    ? TOTAL_BG
               : isSubtotal ? MID_BG
               : isOdd      ? LIGHT
               : 'white';
    const fg   = isCategory ? 'white'
               : isTotal    ? GOLD
               : TEXT;
    const font = (isCategory || isSubtotal || isTotal) ? 'Helvetica-Bold' : 'Helvetica';
    const rh   = isCategory ? ROW_H + 1 : ROW_H;

    doc.rect(x, y, totalW, rh).fill(bg);
    let cx = x;
    for (const col of columns) {
      const val = row[col.key] ?? '';
      doc.fillColor(fg).font(font).fontSize(FS)
         .text(String(val), cx + PAD, y + (rh - FS) / 2 - 0.5,
               { width: col.width - PAD * 2, align: col.align || 'left', lineBreak: false });
      cx += col.width;
    }
    return y + rh;
  }

  let y = drawHeader(startY);
  let oddRow = 0;

  for (const row of rows) {
    const rh = row._type === 'category' ? ROW_H + 1
             : row._type === 'spacer'   ? 6
             : ROW_H;

    if (y + rh > CB) {
      y = addPage(doc);
      y = drawHeader(y);
    }

    y = drawRow(y, row, oddRow % 2 === 1);
    if (!row._type || row._type === 'subtotal') oddRow++;
  }

  return y;
}

