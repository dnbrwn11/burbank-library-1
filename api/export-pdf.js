// ── Format helpers ────────────────────────────────────────────────────────────
const $c  = n => '$' + Math.round(n || 0).toLocaleString('en-US');
const $sf = n => '$' + ((n || 0)).toFixed(2);
const pct = n => ((n || 0) * 100).toFixed(1) + '%';
const short = (s, max = 40) => s && s.length > max ? s.slice(0, max - 1) + '…' : (s || '');

// ── Cost calculation (mirrors src/engine/CostEngine.js) ───────────────────────
const N = v => { const n = Number(v); return isNaN(n) ? 0 : n; };

function itemMid(it)  { return (N(it.qty_min) + N(it.qty_max)) / 2 * N(it.unit_cost_mid); }
function itemLow(it)  { return N(it.qty_min) * N(it.unit_cost_low); }
function itemHigh(it) { return N(it.qty_max) * N(it.unit_cost_high); }

function applyMarkups(base, g) {
  const co  = base * N(g.contingency);
  const gc  = (base + co) * N(g.generalConditions);
  const fe  = (base + co + gc) * N(g.fee);
  const insRate = N(g.insurance) + N(g.bond);
  const ins = (base + co + gc + fe) * insRate;
  const tx  = base * 0.45 * N(g.tax);
  return { base, co, gc, fe, ins, tx, tot: base + co + gc + fe + ins + tx };
}

function computeTotals(items, globals) {
  let lR = 0, mR = 0, hR = 0;
  items.forEach(it => {
    lR += itemLow(it);
    mR += itemMid(it);
    hR += itemHigh(it);
  });
  const factor = (1 + N(globals.escalation)) * N(globals.regionFactor || 1);
  return {
    rawMid: mR, rawLow: lR, rawHigh: hR,
    factor,
    full: {
      l: applyMarkups(lR * factor, globals),
      m: applyMarkups(mR * factor, globals),
      h: applyMarkups(hR * factor, globals),
    },
  };
}

